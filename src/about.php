<h1>A propos de nous</h1>
<p>PoluxSuperShark est un projet Minecraft d'une ville avec des trains moddée. C'est un projet qui a eu ses débuts en 2017
    quand Polux était au collège. Il a découvert Minecraft au collège quand il était en 5ème. Depuis, un projet de ville est
    venu en tête et lui a donné envie de réaliser le projet encore mieux lors de la période de 2O19-2020 pendant la pendémie de
    Covid 19. En 2025, il achète le domaine <a href="poluxsupershark.net" target="_blank">poluxsupershark.net</a> puis le serveur
    Minecraft qu'il paie 17,50 euros par mois et 16 euros par an pour le domaine.
</p>

<p>
    En 2020, il voulait créer un pays sur Minecraft avec des trains, mais sans mod à l'époque. C'est en 2023 qu'il découvre le mod 
    <a href="#">Minecraft Transit Railways</a>. Il a depuis adopé ce mod avec d'autres. En 2024, il tente de créer ce projet pour la 1ère fois
    mais abandonne vite.
</p>

<p>
    En 2025, en mars, il lance officiellement la série mais tarde à la terminer et abandonne encore au bout de 2 mois.
     Il refait un essai voire plusieurs en fin d'année.
</p>

<p>
    Il lance enfin en 2026 avec une vidéo-trailler.
</p>